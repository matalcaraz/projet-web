        <h2 class="page-header">Boulou et Elios Ferré - Guitare</h2>
    </div>
    <div class="col-md-6">
        <p>Ibrahim Maalouf, né le 5 novembre 1980 à Beyrouth, est un trompettiste et pianiste franco-libanais, également compositeur — notamment de musiques de films —, arrangeur, producteur et professeur d'improvisation et de trompette. Son succès est fondé sur un métissage des genres, passant du jazz à la musique orientale ou au rock, parmi de multiples sources d'inspirations</p>
<p>Entre 2002 et 2012, il est régulièrement invité pour donner des master classes et récitals en France et dans le monde, notamment aux États-Unis où il crée un jumelage avec l'université d'État du Kansas qui s'interrompt lors de sa démission du CRR d'Aubervilliers-La Courneuve. Ibrahim est également, pendant plusieurs années, invité pour représenter la France lors de la conférence d'ITG (International Trumpet Guild) qui réunit tous les ans les trompettistes du monde entier pour des concerts et master classes.</p>
