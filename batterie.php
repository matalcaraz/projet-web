        <h2 class="page-header">Manu Katché - Batterie <h2>
    </div>
    <div class="col-md-6">
        <p>Formation classique, Conservatoire National Supérieur de Musique de Paris, à l'origine le musicien Manu Katché est promis à la noble carrière de percussionniste au sein d'un orchestre symphonique. Probablement est-ce là l'une des causes du « son Katché » qui fait que son coup de baguette est reconnu entre mille par les mélomanes avertis. Un coup de baguette qui va le détourner du classique pour le mener au jazz puis au pop-rock. Très vite on le retrouve, musicien de scène et de studio, aux côtés des grands Français : Goldman, Souchon, Chedid, Catherine Lara, Michel Jonasz mais il doit l'explosion de sa carrière à Peter Gabriel qui lui demande de tenir la batterie pour l'album « So ».</p>
<p>Certains musiciens refusent d'être rangés dans une catégorie, au risque de déranger les plus timorés. C'est le privilège de Manu, capable de brasser toutes les influences et de s'adapter à tous les styles.</p>
