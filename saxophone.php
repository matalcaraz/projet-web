        <h2 class="page-header">Louis Sclavis - Saxophone</h2>
    </div>
    <div class="col-md-6">
        <p>Louis Sclavis est très tôt attiré par les instruments de musique, et en possède plusieurs, trompette, violon, tambour, guitare, tous en plastique. Il débute le solfège à 7 ans, puis commence l'étude de la clarinette à l'âge de 9 ans avec un professeur amateur. Au bout de deux ans, il joue dans l'harmonie de Montchat. Pendant cette période, il est exposé à divers styles de musique, du bal musette, de la chanson française, les symphonies de Beethoven, Jean-Christian Michel, des airs d'opérettes, Sydney Bechet et assimile le tout en vrac</p>
<p>Il suit ensuite une formation plus classique en solfège et clarinette dans une annexe du conservatoire de Lyon, où il travaille essentiellement la technique et progresse rapidement. Il commence à jouer dans un petit groupe de new orleans avec des amis, et joue pendant la messe, avec le curé à l'orgue</p>
