        <h2 class="page-header">Benoît Delbecq - Piano </h2>
    </div>
    <div class="col-md-6">
        <p>Benoît Delbecq est un pianiste, compositeur et producteur français né le 6 juin 1966 à Saint Germain en Laye. Issu d'une famille mélomane, Benoît Delbecq grandit à Bougival et commence d'étudier le piano à l'âge de sept ans avec Nicolle Mollard puis bien plus tardivement avec Georges Delvallée, Jay Gottlieb, Sofia Rosoff. Formé à la musique écrite (Analyse et Écriture avec Solange Ancona au conservatoire de Versailles, avec le compositeur David Lacroix) et au jazz (il a été l'élève de Steve Coleman, Dave Holland, Muhal Richard Abrams, Mal Waldron, Alan Silva, Bernard Maury, Jean-Pierre Fouquey), il évolue la scène internationale du jazz contemporain. Avant d'embrasser une carrière de musicien, il a étudié les techniques électroacoustiques, acoustique et a été brièvement assistant-son et assistant-réalisateur.</p>
<p>Lauréat de la Villa Médicis Hors les Murs en 2001, il a reçu en 2009 le Fellowship de la Civitella Fundation (New York). Ses disques"The Sixth Jump" et "Circles and Calligrams" (Songlines, 2010) ont reçu en 2010 un double Grand Prix International du Disque de l'Académie Charles-Cros. Parmi une multitude de récompenses attribuées par la critique internationale, "The Sixth Jump" a été présenté dans la sélection annuelle du New York Times "Best 10 discs of pop/jazz 2010".</p>
