        <h2 class="page-header">Henri Texier - Contrebasse</h2>
    </div>
    <div class="col-md-6">
        <p>Henri Texier, né le 27 janvier 1945 à Paris, est un contrebassiste, multi-instrumentiste, chanteur, chef d'orchestre et compositeur de jazz français.Il naît dans une famille modeste d'émigrés bretons. Son père est poseur de rail à la SNCF et sa mère fait de petits travaux de confection1. Sa famille a toujours gardé un lien très fort avec la Bretagne1. Il commence l'étude du piano vers huit ans, mais les cours de piano sont désagréables1. Un oncle lui fait découvrir le boogie-woogie et il se passionne alors pour le jazz, et constitue un petit orchestre d'amis2. Il découvre des formes de jazz plus modernes en écoutant Misterioso de Thelonious Monk, puis Miles Davis, les Jazz Messengers.</p>
<p>Sa curiosité pour le fait musical breton (Cf. « Varech », et le nom du Septet monté avec son fils) l'a amené à travailler avec des jazzmen armoricains et des pointures de la musique traditionnelle. En 1998, il a notamment participé à l'album Condaghès (Silex/Auvidis) du guitariste Jacques Pellen, avec Paolo Fresu et Erik Marchand (chant). Il a publié tous ses disques en leader avec le label français Label Bleu.</p>
