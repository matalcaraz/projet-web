        <h2 class="page-header">Boulou et Elios Ferré - Guitare</h2>
    </div>
    <div class="col-md-6">
        <p>Boulou Ferré, né Jean-Jacques Ferret à Paris le 24 avril 1951 et Elios Ferré né le 18 décembre 1956, sont deux guitaristes de jazz manouche français. Ils sont les fils de Pierre-Jean Matelo Ferret plus connu sous le nom de Matelo Ferret, cadet d'une famille de musiciens gitans1. Boulou et Elios ont pour oncles Baro et Sarane Ferret qui les initient très tôt à la guitare et aux chants tziganes. Ils forment un duo à partir de 1979, puis un trio avec Michel Graillier, et ensuite Alain Jean-Marie2. C'est avec leur famille qu'ils ont fait leur apprentissage précoce.</p>
<p>Dans la famille de Boulou et Elios, la guitare manouche est omniprésente. Leur père a, comme ses deux frères, accompagné Django Reinhardt et Stéphane Grappelli au Quintette du Hot Club de France.</p>
